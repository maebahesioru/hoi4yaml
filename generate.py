def guess_icon(focus_id):
    hints = get_icon_hints()
    # Jaccard similarity against game focus localisations
    loc_text = _mod_localisation.get(focus_id, "")
    if loc_text and _focus_loc_words:
        query = frozenset(
            w.lower() for w in re.findall(r'\b[a-zA-Z]{4,}\b', loc_text)
            if w.lower() not in _SKIP_WORDS
        )
        if query:
            best_score, best_icon = 0.0, None
            for fid, fwords in _focus_loc_words.items():
                score = len(query & fwords) / len(query | fwords)
                if score > best_score:
                    best_score, best_icon = score, _focus_icons[fid]
            if best_score >= 0.2:
                return best_icon
    # Bigrams from ID end
    words = [w for w in focus_id.lower().split("_")[1:] if w not in _SKIP_WORDS]
    for i in range(len(words) - 1, 0, -1):
        if f"{words[i-1]}_{words[i]}" in hints:
            return hints[f"{words[i-1]}_{words[i]}"]
    for word in reversed(words):
        if word in hints:
            return hints[word]
    return "GFX_focus_generic_political_support"


def auto_layout_focuses(focuses):
    """Assign x/y to focuses that omit them, based on prereq depth + subtree centering."""
    from collections import defaultdict

    prereq_map = {}
    for f in focuses:
        fid = f.get("id")
        if not fid:
            continue
        # Support both prereq (AND) and prereq_or (OR) — combine all for layout purposes
        p = f.get("prereq")
        p_or = f.get("prereq_or")
        all_prereqs = (p if isinstance(p, list) else ([p] if p else [])) + \
                      (p_or if isinstance(p_or, list) else ([p_or] if p_or else []))
        prereq_map[fid] = all_prereqs

    # Spanning tree: primary parent = first valid prereq
    children = defaultdict(list)
    roots = []
    for fid in prereq_map:
        parents = [p for p in prereq_map[fid] if p in prereq_map]
        if parents:
            children[parents[0]].append(fid)
        else:
            roots.append(fid)

    # y = longest path from root
    y_cache = {}
    def get_y(fid, seen=frozenset()):
        if fid in y_cache:
            return y_cache[fid]
        parents = [p for p in prereq_map.get(fid, []) if p in prereq_map and p not in seen]
        y = max((get_y(p, seen | {fid}) + 1 for p in parents), default=0)
        y_cache[fid] = y
        return y
    for fid in prereq_map:
        get_y(fid)

    # Fixed focuses (explicit x or rel_pos) seed the x_map
    x_map = {}
    for f in focuses:
        fid = f.get("id")
        if fid and ("x" in f or "rel_pos" in f):
            x_map[fid] = f.get("x", 0)

    # Post-order: leaves get sequential slots, parents center over children
    counter = [0]
    fixed_ids = {f.get("id") for f in focuses if "x" in f or "rel_pos" in f}
    def assign_x(fid):
        kids = [k for k in children.get(fid, []) if k not in x_map]
        if fid in x_map:
            # Fixed: advance counter past it, still process children
            counter[0] = max(counter[0], x_map[fid] + 2)
            for kid in kids:
                assign_x(kid)
            return
        if not kids:
            x_map[fid] = counter[0]
            counter[0] += 2
        else:
            for kid in kids:
                assign_x(kid)
            xs = [x_map[k] for k in children.get(fid, []) if k in x_map]
            if xs:
                center = (min(xs) + max(xs)) // 2
                x_map[fid] = center + (center % 2)
            else:
                x_map[fid] = counter[0]; counter[0] += 2

    # Fixed roots first (sorted by x), then auto roots
    roots_sorted = sorted(roots, key=lambda fid: x_map.get(fid, float('inf')))
    for root in roots_sorted:
        assign_x(root)

    # Post-process: reposition multi-prereq nodes + their spanning-tree descendants
    multi_prereq = {fid for fid in prereq_map
                    if len([p for p in prereq_map[fid] if p in prereq_map]) > 1}
    # Propagate: find all spanning-tree descendants of multi-prereq nodes
    affected = set(multi_prereq)
    changed = True
    while changed:
        changed = False
        for fid in prereq_map:
            if fid not in affected:
                primary = next((p for p in prereq_map[fid] if p in prereq_map), None)
                if primary in affected:
                    affected.add(fid); changed = True
    # Reposition in topological order (y ascending)
    for fid in sorted(affected, key=lambda f: y_cache.get(f, 0)):
        if fid in fixed_ids:
            continue
        parents = [p for p in prereq_map[fid] if p in x_map]
        if parents:
            ideal = sum(x_map[p] for p in parents) // len(parents)
            x_map[fid] = ideal + (ideal % 2)

    # Apply
    for f in focuses:
        fid = f.get("id")
        if fid and fid in x_map and "x" not in f and "rel_pos" not in f:
            f["x"] = x_map[fid]
            f["y"] = y_cache.get(fid, 0)

    return focuses

def expand_shorthands(data):
    """Expand YAML shorthands to full Clausewitz equivalents."""
    if isinstance(data, list):
        return [expand_shorthands(i) for i in data]
    if not isinstance(data, dict):
        return data

    out = {}
    # Auto-assign focus icon from ID if missing
    if "id" in data and "icon" not in data and ("x" in data or "y" in data or "rel_pos" in data):
        out["icon"] = guess_icon(str(data["id"]))
    for k, v in data.items():
        # Auto-layout focus list before expanding individual items
        if k == "focus" and isinstance(v, list) and v and isinstance(v[0], dict) and "id" in v[0]:
            v = auto_layout_focuses([dict(f) for f in v])
        # prereq: X or [A, B]  →  prerequisite: { focus: X } / { focus: A focus: B } (AND)
        # prereq_or: [A, B]   →  prerequisite: { focus: A } prerequisite: { focus: B } (OR)
        if k == "prereq":
            if isinstance(v, list):
                # List = AND (all required in one block)
                out["prerequisite"] = {f"focus": v[0]} if len(v) == 1 else {"focus": v}
            else:
                out["prerequisite"] = {"focus": v}
        elif k == "prereq_or":
            # OR: multiple prerequisite blocks
            items = v if isinstance(v, list) else [v]
            out["prerequisite"] = [{"focus": f} for f in items]
        # ai_will_do: N  →  ai_will_do: { factor: N }
        # Calculation: result = factor * modifier1.factor * modifier2.factor * ...
        # factor: 0 = AI never picks this; factor: 1 with modifiers = conditional weight
        # if: [{cond: val, mult: N}]  →  modifier: { factor: N, cond: val }
        elif k == "ai_will_do":
            if isinstance(v, (int, float)):
                out["ai_will_do"] = {"factor": v}
            elif isinstance(v, dict) and "factor" in v:
                modifiers = []
                base = {"factor": v["factor"]}
                for mk, mv in v.items():
                    if mk == "factor":
                        continue
                    # Shorthand: if: {cond: val, mult: N} → modifier: { factor: N, cond: val }
                    if mk == "if" and isinstance(mv, list):
                        for item in mv:
                            mod = {"factor": item.pop("mult", 1)}
                            mod.update(item)
                            modifiers.append(mod)
                    elif mk == "if" and isinstance(mv, dict):
                        mod = {"factor": mv.pop("mult", 1)}
                        mod.update(mv)
                        modifiers.append(mod)
                result = dict(base)
                if modifiers:
                    result["modifier"] = modifiers
                out["ai_will_do"] = expand_shorthands(result)
            else:
                out["ai_will_do"] = expand_shorthands(v)
        elif k == "reward":
            out["completion_reward"] = expand_shorthands(v)
        # rel_pos: X  →  relative_position_id: X
        elif k == "rel_pos":
            out["relative_position_id"] = v
        # hidden: {...}  →  hidden_effect: {...}
        # NOTE: Use hidden_effect for effects you don't want shown in the event log
        # owner vs controller: owner = who owns the state (even if occupied), controller = who controls it now
        # set_variable: overwrites value; add_to_variable: adds to existing value (use clamp_variable after)
        # save_event_target_as: must be in same scope as FROM/ROOT when referenced later
        # limit: goes INSIDE every_state/every_country blocks, not outside
        # modifier (tech): country-level bonus; equipment_bonus: applies to equipment stats
        # on_action timing: on_war_won fires when war ends, on_capitulation when country capitulates
        # set_country_flag scope: fires on the CURRENT scope's country, use ROOT/FROM to target others
        elif k == "hidden":
            out["hidden_effect"] = expand_shorthands(v)
        # timed_idea: {idea: X, days: N}  →  add_ideas + remove_ideas with days_remove
        elif k == "timed_idea" and isinstance(v, dict):
            idea = v["idea"]
            days = v.get("days", 30)
            out["add_ideas"] = idea
            out["if"] = {"limit": {"NOT": {"has_idea": idea}},
                         "add_ideas": idea}
            # days_remove is set on the idea itself; emit a comment-style key
            out["_timed_idea_note"] = f"Set days_remove = {days} on idea '{idea}' definition"
        # division_template regiment/support shorthand
        # regiments: [{infantry: 3}, {artillery: 1}]  →  regiment blocks
        elif k == "regiments" and isinstance(v, list):
            out["regiment"] = [{"type": list(r.keys())[0], "column": i % 5, "row": i // 5}
                               for i, r in enumerate(v) for _ in range(list(r.values())[0])]
        elif k == "support_companies" and isinstance(v, list):
            out["support"] = [{"type": list(s.keys())[0], "column": i, "row": 0}
                              for i, s in enumerate(v)]
            out["hidden_effect"] = expand_shorthands(v)
        # add_state_building: {state: X, type: T, level: N, instant: true}
        # → state_id = { add_building_construction = { type = T level = N instant_build = yes } }
        elif k == "add_state_building" and isinstance(v, dict):
            sid = resolve_state(v["state"]) if "state" in v else v.get("state_id")
            payload = expand_shorthands({"add_building_construction": {
                "type": v["type"], "level": v.get("level", 1),
                "instant_build": "yes" if v.get("instant", True) else "no"
            }})
            out[sid] = {**out[sid], **payload} if sid in out and isinstance(out.get(sid), dict) else payload
        elif k == "add_state_manpower" and isinstance(v, dict):
            sid = resolve_state(v["state"]) if "state" in v else v.get("state_id")
            payload = {"add_manpower": v["value"]}
            out[sid] = {**out[sid], **payload} if sid in out and isinstance(out.get(sid), dict) else payload
        # sprites: [...]  →  spriteTypes: { spriteType: [...] }
        elif k == "sprites":
            out["spriteTypes"] = {"spriteType": expand_shorthands(v)}
        # set_tech: [tech1, tech2]  →  set_technology: { tech1: 1, tech2: 1 }
        elif k == "set_tech":
            techs = v if isinstance(v, list) else [v]
            out["set_technology"] = {t: 1 for t in techs}
        # set_ruling_party: X  →  set_politics: { ruling_party: X, elections_allowed: no, election_frequency: 48 }
        elif k == "set_ruling_party":
            out["set_politics"] = {"ruling_party": v, "elections_allowed": "no", "election_frequency": 48}
        # add_pop: { fascism: 0.1 }  →  add_popularity: { ideology: fascism, popularity: 0.1 }
        elif k == "add_pop" and isinstance(v, dict):
            for ideology, pop in v.items():
                out.setdefault("add_popularity", [])
                if not isinstance(out["add_popularity"], list):
                    out["add_popularity"] = [out["add_popularity"]]
                out["add_popularity"].append({"ideology": ideology, "popularity": pop})
        # color without color_ui  →  duplicate color as color_ui
        elif k == "color" and "color_ui" not in data:
            out["color"] = v
            out["color_ui"] = v
        else:
            out[k] = expand_shorthands(v)
    return out


