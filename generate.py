def load_gfx_from_game(hoi4_path):
    gfx = set()
    for gfx_file in hoi4_path.glob("interface/*.gfx"):
        text = gfx_file.read_text(encoding="utf-8-sig", errors="ignore")
        gfx.update(re.findall(r'name\s*=\s*"?(GFX_\w+)"?', text))
    return gfx


def extract_mod_gfx(config):
    """Extract GFX keys defined in this mod's interface section."""
    gfx = set()
    for entry in config.get("interface", []):
        for sprite in entry.get("sprites", []):
            if "name" in sprite:
                gfx.add(sprite["name"])
        # Also handle raw spriteTypes
        st = entry.get("spriteTypes", {})
        for s in (st.get("spriteType") or []):
            if isinstance(s, dict) and "name" in s:
                gfx.add(s["name"])
    return gfx


def extract_mod_states(config):
    """Extract state name→id from this mod's history_states section."""
    state_map = {}
    for entry in config.get("history_states", []):
        state = entry.get("state", {})
        sid = state.get("id")
        name = state.get("name", "")
        if sid and name.startswith("STATE_"):
            # Try to get human name from localisation
            loc = config.get("localisation", {})
            if isinstance(next(iter(loc.values()), None), dict):
                loc = loc.get("english", {})
            human = loc.get(name, "").lower()
            if human:
                state_map[human] = int(sid)
    return state_map


def load_extra_mod_paths(mod_paths):
    """Scan additional mod directories for GFX keys, state IDs, localisation keys, traits, and ideologies."""
    extra_gfx = set()
    extra_states = {}
    extra_loc_keys = set()
    extra_traits = set()
    extra_ideologies = set()
    for path_str in (mod_paths or []):
        p = Path(path_str).expanduser()
        if not p.exists():
            warn(f"mod_path not found: {p}")
            continue
        # GFX from interface files
        for f in p.glob("interface/*.gfx"):
            extra_gfx.update(re.findall(r'name\s*=\s*"?(GFX_\w+)"?',
                f.read_text(encoding="utf-8-sig", errors="ignore")))
        # States from history/states
        for f in (p / "history/states").glob("*.txt") if (p / "history/states").exists() else []:
            text = f.read_text(encoding="utf-8-sig", errors="ignore")
            id_m  = re.search(r'\bid\s*=\s*(\d+)', text)
            key_m = re.search(r'\bname\s*=\s*"(STATE_\d+)"', text)
            if id_m and key_m:
                extra_states[key_m.group(1)] = int(id_m.group(1))
        # Localisation keys
        for f in p.glob("localisation/**/*.yml"):
            for m in re.finditer(r'^\s*(\S+):\d+\s+"', f.read_text(encoding="utf-8-sig", errors="ignore"), re.MULTILINE):
                extra_loc_keys.add(m.group(1))
        # Traits
        for trait_dir in ("common/country_leader", "common/unit_leader"):
            for f in (p / trait_dir).glob("*.txt") if (p / trait_dir).exists() else []:
                text = f.read_text(encoding="utf-8-sig", errors="ignore")
                for block in re.findall(r'leader_traits\s*=\s*\{(.+?)^\}', text, re.DOTALL | re.MULTILINE):
                    for m in re.finditer(r'^\t(\w+)\s*=\s*\{', block, re.MULTILINE):
                        extra_traits.add(m.group(1))
        # Ideologies
        for f in (p / "common/ideologies").glob("*.txt") if (p / "common/ideologies").exists() else []:
            text = f.read_text(encoding="utf-8-sig", errors="ignore")
            for m in re.finditer(r'^\s*(\w+)\s*=\s*\{', text, re.MULTILINE):
                extra_ideologies.add(m.group(1))
    return extra_gfx, extra_states, extra_loc_keys, extra_traits, extra_ideologies


def validate(config):
    # Known GFX: game + this mod's own sprites + extra mod paths
    known_gfx = set()
    hoi4_path = find_hoi4_path()
    if hoi4_path:
        known_gfx = load_gfx_from_game(hoi4_path)
    known_gfx |= extract_mod_gfx(config)
    extra_gfx, _, extra_loc_keys, extra_traits, extra_ideologies = load_extra_mod_paths(config.get("mod", {}).get("mod_paths", []))
    known_gfx |= extra_gfx

    # Merge sub-mod traits/ideologies into global sets for validation
    get_game_maps()
    if extra_traits and _trait_set is not None:
        _trait_set.update(extra_traits)
        if _country_leader_trait_set is not None:
            _country_leader_trait_set.update(extra_traits)
        if _unit_leader_trait_set is not None:
            _unit_leader_trait_set.update(extra_traits)
    if extra_ideologies and _ideology_set is not None:
        _ideology_set.update(extra_ideologies)

    loc = config.get("localisation", {})
    loc_keys = set()
    if loc and isinstance(next(iter(loc.values())), dict):
        for entries in loc.values():
            loc_keys.update(entries.keys())
    else:
        loc_keys.update(loc.keys())
    loc_keys |= extra_loc_keys

    focus_ids = []
    for entry in config.get("national_focus", []):
        for focus in (entry.get("focus_tree") or {}).get("focus", []):
            focus_ids.append(focus.get("id"))
            icon = focus.get("icon")
            if icon and known_gfx and icon not in known_gfx:
                warn(f"Unknown GFX key: {icon}")
    for d in {x for x in focus_ids if focus_ids.count(x) > 1}:
        warn(f"Duplicate focus id: {d}")

    # Collect event IDs referenced anywhere in the config (they need is_triggered_only)
    referenced_event_ids = set()
    def _collect_all_event_refs(d):
        if isinstance(d, list):
            for i in d: _collect_all_event_refs(i)
        elif isinstance(d, dict):
            for k, v in d.items():
                if k in ("country_event", "news_event", "state_event", "random_events"):
                    if isinstance(v, (str, int)):
                        referenced_event_ids.add(str(v))
                    elif isinstance(v, dict) and "id" in v:
                        referenced_event_ids.add(str(v["id"]))
                    else:
                        _collect_all_event_refs(v)
                else:
                    _collect_all_event_refs(v)
    _collect_all_event_refs(config)

    event_ids = []
    for entry in config.get("events", []):
        for etype in ("country_event", "news_event", "state_event"):
            for ev in (entry.get(etype) or []):
                event_ids.append(ev.get("id"))
                eid = str(ev.get("id", ""))
                if (not ev.get("is_triggered_only") and not ev.get("mean_time_to_happen")
                        and eid not in referenced_event_ids):
                    warn(f"Event '{eid}' has neither 'is_triggered_only' nor 'mean_time_to_happen' — it will never fire")
                for key in ("title", "desc"):
                    val = ev.get(key)
                    if val and val not in loc_keys:
                        warn(f"Missing localisation: {val}")
                for opt in (ev.get("option") or []):
                    name = opt.get("name")
                    if not name:
                        warn(f"Event '{ev.get('id')}' has an option without 'name' — localisation key required")
                    elif name not in loc_keys:
                        warn(f"Missing localisation: {name}")
    for d in {x for x in event_ids if event_ids.count(x) > 1}:
        warn(f"Duplicate event id: {d}")

    # Ideology / trait / tech_category validation (requires game data)
    get_game_maps()  # ensure globals loaded
    if _ideology_set or _trait_set or _tech_category_set:
        import difflib

        # Context-aware keys that indicate leader type
        COUNTRY_LEADER_KEYS = {"country_leader"}
        UNIT_LEADER_KEYS = {"field_marshal", "corps_commander", "navy_leader"}
        IDEOLOGY_KEYS = {"ideology", "ruling_party"}
        EFFECT_CTX_KEYS = {"completion_reward", "option", "immediate", "after", "hidden_effect"}

        def _check_recursive(data, ctx=None):
            if isinstance(data, list):
                for i in data: _check_recursive(i, ctx)
            elif isinstance(data, dict):
                for k, v in data.items():
                    new_ctx = ctx
                    if k in COUNTRY_LEADER_KEYS:
                        new_ctx = "country_leader"
                    elif k in UNIT_LEADER_KEYS:
                        new_ctx = "unit_leader"
                    elif k in EFFECT_CTX_KEYS:
                        new_ctx = k
                    elif k in ("regiment", "support"):
                        new_ctx = "regiment"
                    elif k in ("add_equipment_to_stockpile", "equipment"):
                        new_ctx = "equipment"

                    if k in IDEOLOGY_KEYS and isinstance(v, str) and _ideology_set and v not in _ideology_set:
                        close = difflib.get_close_matches(v, _ideology_set, n=3, cutoff=0.5)
                        warn(f"Unknown ideology: '{v}'" + (f" — did you mean: {close}?" if close else ""))
                    elif k == "traits" and isinstance(v, list):
                        valid = (_country_leader_trait_set if ctx == "country_leader"
                                 else _unit_leader_trait_set if ctx == "unit_leader"
                                 else _trait_set)
                        ctx_label = f" ({ctx})" if ctx else ""
                        for t in v:
                            if isinstance(t, str) and valid and t not in valid:
                                close = difflib.get_close_matches(t, valid, n=3, cutoff=0.5)
                                warn(f"Unknown trait{ctx_label}: '{t}'" + (f" — did you mean: {close}?" if close else ""))
                    elif k == "categories" and isinstance(v, list) and _tech_category_set:
                        for c in v:
                            if isinstance(c, str) and c not in _tech_category_set:
                                close = difflib.get_close_matches(c, _tech_category_set, n=3, cutoff=0.5)
                                warn(f"Unknown tech category: '{c}'" + (f" — did you mean: {close}?" if close else ""))
                    elif k in ("add_building_construction", "set_building_level") and _building_max:
                        btype = v.get("type") if isinstance(v, dict) else None
                        level = v.get("level") if isinstance(v, dict) else (v if isinstance(v, int) else None)
                        if btype and level and btype in _building_max and level > _building_max[btype]:
                            warn(f"Building '{btype}' level {level} exceeds max {_building_max[btype]}")
                    elif k in _building_max and isinstance(v, int) and v > _building_max[k]:
                        warn(f"Building '{k}' level {v} exceeds max {_building_max[k]}")
                    elif k == "resources" and isinstance(v, dict) and _resource_set:
                        for rk in v:
                            if rk not in _resource_set:
                                import difflib
                                close = difflib.get_close_matches(rk, _resource_set, n=3, cutoff=0.4)
                                warn(f"Unknown resource: '{rk}'" + (f" — did you mean: {close}?" if close else f" — valid: {sorted(_resource_set)}"))
                    elif k in ("add_resource",) and isinstance(v, dict) and _resource_set:
                        rtype = v.get("type")
                        if rtype and rtype not in _resource_set:
                            import difflib
                            close = difflib.get_close_matches(rtype, _resource_set, n=3, cutoff=0.4)
                            warn(f"Unknown resource: '{rtype}'" + (f" — did you mean: {close}?" if close else f" — valid: {sorted(_resource_set)}"))
                    elif k == "type" and isinstance(v, str) and ctx == "regiment" and _sub_unit_set and v not in _sub_unit_set:
                        import difflib
                        close = difflib.get_close_matches(v, _sub_unit_set, n=3, cutoff=0.5)
                        warn(f"Unknown unit type: '{v}'" + (f" — did you mean: {close}?" if close else ""))
                    elif k == "type" and isinstance(v, str) and ctx in ("equipment", "add_equipment_to_stockpile") and _equipment_type_set and v not in _equipment_type_set:
                        import difflib
                        close = difflib.get_close_matches(v, _equipment_type_set, n=3, cutoff=0.5)
                        warn(f"Unknown equipment type: '{v}'" + (f" — did you mean: {close}?" if close else ""))
                    elif k == "ai_will_do" and isinstance(v, dict) and v.get("factor") == 0:
                        warn(f"ai_will_do factor: 0 — AI will never pick this (use factor: 1 with modifiers instead)")
                    elif k == "modifier" and isinstance(v, dict) and ctx in EFFECT_CTX_KEYS:
                        warn(f"'modifier' block in effect context ('{ctx}') does nothing — use 'add_modifier' or 'add_ideas' instead")
                        _check_recursive(v, new_ctx)
                    elif k == "add_core_of" and isinstance(v, (int, float)):
                        warn(f"'add_core_of' takes a country TAG (e.g. GER), not a number — did you mean 'add_core = {v}'?")
                    elif k == "add_core" and isinstance(v, str) and re.match(r'^[A-Z]{2,3}$', v):
                        warn(f"'add_core' takes a state ID (number), not a TAG — did you mean 'add_core_of = {v}'?")
                    elif k == "set_technology" and isinstance(v, dict) and "type" in v:
                        warn(f"'set_technology' does not use a 'type' key — use '{{ tech_name: 1 }}' directly, e.g. set_technology: {{ {v.get('type')}: 1 }}")
                    elif k == "add_tech_bonus" and isinstance(v, dict) and "category" not in v:
                        warn(f"'add_tech_bonus' missing 'category' — it will not work without specifying a tech category")
                    elif k == "set_politics" and isinstance(v, dict):
                        for req in ("ruling_party", "elections_allowed", "election_frequency"):
                            if req not in v:
                                warn(f"'set_politics' missing '{req}' — all three fields are required")
                        _check_recursive(v, new_ctx)
                    elif k == "add_popularity" and isinstance(v, dict):
                        for req in ("ideology", "popularity"):
                            if req not in v:
                                warn(f"'add_popularity' missing '{req}' — use add_popularity: {{ ideology: X, popularity: 0.1 }}")
                        _check_recursive(v, new_ctx)
                    elif k == "create_wargoal" and isinstance(v, dict):
                        if "type" not in v:
                            warn(f"'create_wargoal' missing 'type'")
                        elif _wargoal_type_set and v["type"] not in _wargoal_type_set:
                            import difflib
                            close = difflib.get_close_matches(v["type"], _wargoal_type_set, n=3, cutoff=0.4)
                            warn(f"Unknown wargoal type: '{v['type']}'" + (f" — did you mean: {close}?" if close else f" — valid: {sorted(_wargoal_type_set)}"))
                        if "target" not in v:
                            warn(f"'create_wargoal' missing 'target' (country TAG)")
                        _check_recursive(v, new_ctx)
                    elif k == "equipment_bonus" and isinstance(v, dict):
                        for eq_key, eq_val in v.items():
                            if isinstance(eq_val, dict) and "instant" not in eq_val:
                                warn(f"'equipment_bonus.{eq_key}' missing 'instant: yes' — bonus only applies to newly produced equipment, not existing stockpile")
                        _check_recursive(v, new_ctx)
                    elif k == "modifier" and isinstance(v, dict) and ctx not in EFFECT_CTX_KEYS and _modifier_name_set:
                        for mk in v:
                            if mk not in _modifier_name_set:
                                import difflib
                                close = difflib.get_close_matches(mk, _modifier_name_set, n=3, cutoff=0.5)
                                warn(f"Unknown modifier: '{mk}'" + (f" — did you mean: {close}?" if close else ""))
                        _check_recursive(v, new_ctx)
                    elif k == "random_list" and isinstance(v, dict):
                        for weight, _ in v.items():
                            try:
                                float(str(weight))
                            except ValueError:
                                warn(f"random_list key '{weight}' is not a number (weights must be numeric)")
                        _check_recursive(v, new_ctx)
                    elif k in ("completion_reward", "option", "immediate", "after", "hidden_effect") and isinstance(v, (dict, list)):
                        # Check for trigger-only keywords used as effects
                        TRIGGER_ONLY = {"has_war", "is_major", "tag", "has_government", "has_idea",
                                        "has_tech", "has_stability", "has_war_support", "num_of_factories",
                                        "is_ai", "has_full_control_of_state", "has_capitulated"}
                        # Check for effect-only keywords used as triggers (in trigger/limit blocks)
                        EFFECT_ONLY = {"add_political_power", "add_stability", "add_war_support",
                                       "add_manpower", "add_equipment_to_stockpile", "set_politics",
                                       "add_ideas", "remove_ideas", "start_war", "white_peace"}
                        def _find_triggers(d, ctx_key):
                            if isinstance(d, list):
                                for i in d: _find_triggers(i, ctx_key)
                            elif isinstance(d, dict):
                                for dk, dv in d.items():
                                    if dk in TRIGGER_ONLY:
                                        warn(f"'{dk}' is a trigger, not an effect — found in '{ctx_key}' block")
                                    elif dk not in ("limit", "trigger"):
                                        _find_triggers(dv, ctx_key)
                        def _find_effects_in_triggers(d, ctx_key):
                            if isinstance(d, list):
                                for i in d: _find_effects_in_triggers(i, ctx_key)
                            elif isinstance(d, dict):
                                for dk, dv in d.items():
                                    if dk in EFFECT_ONLY:
                                        warn(f"'{dk}' is an effect, not a trigger — found in '{ctx_key}' block")
                                    else:
                                        _find_effects_in_triggers(dv, ctx_key)
                        _find_triggers(v, k)
                        # Check trigger/limit sub-blocks for effects
                        def _scan_for_trigger_blocks(d):
                            if isinstance(d, list):
                                for i in d: _scan_for_trigger_blocks(i)
                            elif isinstance(d, dict):
                                for sub_k in ("trigger", "limit"):
                                    if sub_k in d:
                                        _find_effects_in_triggers(d[sub_k], sub_k)
                                for dv in d.values():
                                    if isinstance(dv, (dict, list)):
                                        _scan_for_trigger_blocks(dv)
                        _scan_for_trigger_blocks(v)
                        _check_recursive(v, new_ctx)
                    # news_event fired from effect context → likely should be country_event
                    elif k == "news_event" and ctx in EFFECT_CTX_KEYS:
                        warn(f"'news_event' fires for ALL countries — use 'country_event' if targeting specific countries only")
                        _check_recursive(v, new_ctx)
                    # add_to_variable without nearby clamp_variable
                    elif k == "add_to_variable" and isinstance(data, dict) and "clamp_variable" not in data:
                        warn(f"'add_to_variable' without 'clamp_variable' — variable may go negative or overflow")
                        _check_recursive(v, new_ctx)
                    # set_country_flag scope note
                    elif k == "set_country_flag" and ctx in EFFECT_CTX_KEYS:
                        # Just recurse; scope is user's responsibility
                        _check_recursive(v, new_ctx)
                    else:
                        _check_recursive(v, new_ctx)
        _check_recursive(config)

    # Check remove_ideas against ideas with removal_cost = -1
    non_removable = set()
    for entry in config.get("ideas", []):
        if "_category" in entry:
            # _category style: idea defs are direct non-underscore keys
            for idea_name, idea_def in entry.items():
                if not idea_name.startswith("_") and isinstance(idea_def, dict):
                    if idea_def.get("removal_cost") == -1:
                        non_removable.add(idea_name)
        else:
            # Regular style: ideas: { category: { idea_name: {...} } }
            for cat_ideas in entry.values():
                if isinstance(cat_ideas, dict):
                    for idea_name, idea_def in cat_ideas.items():
                        if isinstance(idea_def, dict) and idea_def.get("removal_cost") == -1:
                            non_removable.add(idea_name)
    if non_removable:
        def _find_remove_ideas(data):
            if isinstance(data, list):
                for i in data: _find_remove_ideas(i)
            elif isinstance(data, dict):
                if "remove_ideas" in data:
                    targets = data["remove_ideas"]
                    if isinstance(targets, str): targets = [targets]
                    for t in (targets if isinstance(targets, list) else []):
                        if t in non_removable:
                            warn(f"'remove_ideas: {t}' — this idea has removal_cost: -1 (cannot be removed)")
                for v in data.values():
                    _find_remove_ideas(v)
        _find_remove_ideas(config)

    # Warn if 'character' (singular) used as top-level section (should be 'characters')
    if "character" in config:
        warn(f"Top-level key 'character' found — did you mean 'characters'?")

    # Technology: missing path or folder
    for entry in config.get("technologies", []):
        for tech_name, tech_def in entry.items():
            if tech_name.startswith("_") or not isinstance(tech_def, dict):
                continue
            if "path" not in tech_def and "folder" not in tech_def and "on_research_complete" not in tech_def:
                warn(f"Technology '{tech_name}' has no 'path' or 'folder' — it will not appear in the tech tree")

    # history_countries: capital should be a province ID (typically > 1000), not a state ID
    for entry in config.get("history_countries", []):
        cap = entry.get("capital") or (entry.get("history") or {}).get("capital")
        if cap and isinstance(cap, int) and cap < 1000:
            warn(f"'capital = {cap}' looks like a state ID — 'capital' takes a province ID (usually > 1000)")

    # load_oob: referenced file must exist in history_units section
    oob_files = {entry.get("_file") for entry in config.get("history_units", [])}
    def _find_load_oob(d):
        if isinstance(d, list):
            for i in d: _find_load_oob(i)
        elif isinstance(d, dict):
            if "load_oob" in d:
                ref = d["load_oob"]
                if isinstance(ref, str) and ref not in oob_files:
                    warn(f"'load_oob = {ref}' — no matching history_units entry with _file: {ref}")
            for v in d.values():
                _find_load_oob(v)
    _find_load_oob(config)


